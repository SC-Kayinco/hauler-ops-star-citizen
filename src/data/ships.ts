import type { BayBaseFace, BayMount, CargoBay, DoorEdge, Ship } from '@/types'

/**
 * Build a simple rectangular cargo bay.
 * width x length x maxStackHeight (in SCU cells) ~= bay capacity in SCU.
 */
export function makeBay(
  id: string,
  name: string,
  width: number,
  length: number,
  maxStackHeight: number,
  opts: {
    doorEdge?: DoorEdge
    maxContainerScu?: number
    mount?: BayMount
    side?: 'left' | 'right'
    baseFace?: BayBaseFace
  } = {},
): CargoBay {
  return {
    id,
    name,
    width,
    length,
    maxStackHeight,
    doorEdge: opts.doorEdge ?? 'back',
    blockedCells: [],
    maxContainerScu: opts.maxContainerScu,
    mount: opts.mount ?? 'floor',
    side: opts.side,
    baseFace: opts.baseFace,
  }
}

